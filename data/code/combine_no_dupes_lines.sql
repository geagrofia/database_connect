-- join the two tables
DROP TABLE IF EXISTS  COVERAGE.FRICTION_B_LINES_COMBINED_NO_DUPES CASCADE;

CREATE TABLE COVERAGE.FRICTION_B_LINES_COMBINED_NO_DUPES AS
(SELECT * FROM COVERAGE.FRICTION_B_LINES_CARDINAL_NO_DUPES 
UNION
SELECT * FROM  COVERAGE.FRICTION_B_LINES_ORDINAL_NO_DUPES 
);

-- add a unique id 
ALTER TABLE COVERAGE.FRICTION_B_LINES_COMBINED_NO_DUPES ADD COLUMN comb_id SERIAL PRIMARY KEY;

